
CREATE FUNCTION HashPassword(@pass VARCHAR(100))
RETURNS NVARCHAR(500)
AS
BEGIN
	DECLARE @afterHash VARBINARY(500) = HASHBYTES('SHA2_256', @pass)
	RETURN CONVERT(NVARCHAR(1000), @afterHash, 2)
END

/*------------- FOR CHECKING ------------------------*/
GO;

